# Definitions.

# str - sequence       of immutable, ordered,   arbitrary Unicode symbols:          'python'
# list - sequence      of mutable,   ordered,   arbitrary elements:                 ['p', 'y', 't', 'h', 'o', 'n']
# tuple - sequence     of immutable, ordered,   arbitrary elemets:                  ('p', 'y', 't', 'h', 'o', 'n')
# dict - mapping       of mutable,   ordered,   unique keys and arbitrary values:   {1 : 'L', 2 :'O', 3 : 'L'}
# set - sequence       of mutable,   unordered, unique elements:                    {'apple', 'banana', 'cherry'}
# frozenset - sequence of immutable, unordered, unique elements:                    (['apple', 'banana', 'cherry'])

# Hashable / Immutable - means that the elements of an object (i.e. tuple, frozenset, byte) cannot be...
